def normalize_character_name(name: str) -> str:
    name = name.strip()
    return ALIASES.get(name, name)


def _pair_key(a: str, b: str) -> frozenset:
    return frozenset({normalize_character_name(a), normalize_character_name(b)})


def _build_pair_index() -> dict[frozenset, list[str]]:
    idx: dict[frozenset, list[str]] = {}
    for x, y, tags in RELATION_EDGES:
        key = frozenset({x, y})
        idx.setdefault(key, [])
        for t in tags:
            if t not in idx[key]:
                idx[key].append(t)
    return idx


_PAIR_TO_TAGS = _build_pair_index()


def lookup_relations(name_a: str, name_b: str) -> list[str]:
    key = _pair_key(name_a, name_b)
    return list(_PAIR_TO_TAGS.get(key, []))


def iter_edges_for_nodes(node_set: set):
    for x, y, tags in RELATION_EDGES:
        if x in node_set and y in node_set:
            yield x, y, tags


def iter_all_edges():
    yield from RELATION_EDGES


def neighbors_of(name: str) -> list[tuple[str, list[str]]]:
    out: list[tuple[str, list[str]]] = []
    for x, y, tags in RELATION_EDGES:
        if x == name:
            out.append((y, tags))
        elif y == name:
            out.append((x, tags))
    return out


def faction_of(name: str) -> str | None:
    return CHARACTER_FACTION.get(normalize_character_name(name))


def faction_meta(fid: str) -> dict:
    return FACTIONS[fid]


def edge_label(tags: list[str]) -> str:
    return "；".join(tags)


def is_same_faction(a: str, b: str) -> bool:
    fa, fb = faction_of(a), faction_of(b)
    return fa is not None and fa == fb


def classify_edge(a: str, b: str) -> str:
    if is_same_faction(a, b):
        return "阵营内"
    fa, fb = faction_of(a), faction_of(b)
    if fa and fb:
        return "阵营间"
    return "其他"


def iter_edges_for_faction(fid: str):
    members = set(FACTIONS[fid]["members"])
    for x, y, tags in RELATION_EDGES:
        if x in members or y in members:
            yield x, y, tags
